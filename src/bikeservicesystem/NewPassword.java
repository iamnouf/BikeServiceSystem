             String NewPassword = jPasswordField1.getText();
        String ConfirmPassword =jPasswordField2.getText();

        String upperCase = "(.*[A-Z].*)";
        String numbers = "(.*[0-9].*)";
        String specialChars = "(.*[ ! # @ $ % ^ & * ( ) - _ = + [ ] ; : ' \" , < . > / ?].*)";
